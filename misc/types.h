#ifndef SDISK_MISC_TYPES_H
#define SDISK_MISC_TYPES_H
/* 8-bit data type */
typedef char            s8;
typedef unsigned char   u8;

/* 16-bit data type */
typedef short           s16;
typedef unsigned short  u16;

/* 32-bit data type */
typedef long            s32;
typedef unsigned long   u32;

/* 64-bit data type */
typedef long long          s64;
typedef unsigned long long u64;

#endif
